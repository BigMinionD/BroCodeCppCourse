#include <iostream>

int main(){
    // Declaration and assignment of x
    int x;
    x = 5;

    // Initialization of y, declares and assigns a variable in the same statement
    int y = 6;
    int sum = x + y;

    std::cout << x << '\n';
    std::cout << y << '\n';
    std::cout << sum << '\n';

    return 0;
}