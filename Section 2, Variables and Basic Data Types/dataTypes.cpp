#include <iostream>

int main(){
    // int (whole numbers)
    int age = 18;
    int year = 2025;
    int days = 7;

    // double (number includes decimal)
    double price = 10.99;
    double gpa = 2.5;
    double temperature = 25.1;

    // char (a single character)
    char grade = 'A';
    char initial = 'C';
    char dollarSign = '$';

    // bool (true or false)
    bool student = false;
    bool power = true;
    bool forSale = true;

    // string (sequences of text)
    std::string name = "Bro";
    std::string day = "Friday";
    std::string food = "pizza";
    std::string address = "123 Fake St.";

    return 0;
}